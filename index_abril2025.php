         function formatCurrency(input) {
             input.addEventListener('input', (e) => {
                 let value = e.target.value;
             value = value.replace(/[^0-9]/g, ''); // Elimina caracteres no numéricos
             value = value.replace(/\B(?=(\d{3})+(?!\d))/g, ','); // Añade comas como separadores de miles
             if (value) {
                 value = '$' + value;
             } else {
                 value = ''; // Si está vacío, no muestra nada
             }
             e.target.value = value;
         });
         
         // Elimina caracteres especiales al pegar
             input.addEventListener('paste', (e) => {
                 let paste = (e.clipboardData || window.clipboardData).getData('text');
                 paste = paste.replace(/[^0-9]/g, '');
                 e.preventDefault();
                 document.execCommand('insertText', false, paste);
             });
         
         // Valida el monto mínimo y si está vacío
             input.addEventListener('blur', validateInput);
